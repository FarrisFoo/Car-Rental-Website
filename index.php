<script>
const hslides = document.querySelectorAll('.hslides');
const prev = document.querySelector('.hprev');
const next = document.querySelector('.hnext');
i = 0;

function ActiveSlide(n){
  for(slide of hslides)
  slide.classList.remove('active');
  hslides[n].classList.add('active');
}

// for prev btn
prev.addEventListener('click', function(){
  if(i == 0){
    i = hslides.length - 1;
    ActiveSlide(i);
  }
  else{
    i--;
    ActiveSlide(i);
  }
})

// for next btn
next.addEventListener('click', function(){
  if(i == hslides.length - 1){
    i = 0;
    ActiveSlide(i);
  }
  else{
    i++;
    ActiveSlide(i);
  }
})

</script>
